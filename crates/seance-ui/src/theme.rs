use gpui::Hsla;

fn solid(hex: u32) -> Hsla {
    gpui::rgb(hex).into()
}

fn alpha(hex: u32, a: f32) -> Hsla {
    let mut c: Hsla = gpui::rgb(hex).into();
    c.a = a;
    c
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum ThemeId {
    ObsidianSmoke,
    MidnightFrost,
    Bone,
    Phosphor,
    TokyoNight,
    CatppuccinMocha,
    RosePine,
    Dracula,
    Nord,
    SolarizedDark,
}

impl ThemeId {
    pub const ALL: &[ThemeId] = &[
        ThemeId::ObsidianSmoke,
        ThemeId::MidnightFrost,
        ThemeId::Bone,
        ThemeId::Phosphor,
        ThemeId::TokyoNight,
        ThemeId::CatppuccinMocha,
        ThemeId::RosePine,
        ThemeId::Dracula,
        ThemeId::Nord,
        ThemeId::SolarizedDark,
    ];

    pub fn theme(&self) -> Theme {
        match self {
            ThemeId::ObsidianSmoke => obsidian_smoke(),
            ThemeId::MidnightFrost => midnight_frost(),
            ThemeId::Bone => bone(),
            ThemeId::Phosphor => phosphor(),
            ThemeId::TokyoNight => tokyo_night(),
            ThemeId::CatppuccinMocha => catppuccin_mocha(),
            ThemeId::RosePine => rose_pine(),
            ThemeId::Dracula => dracula(),
            ThemeId::Nord => nord(),
            ThemeId::SolarizedDark => solarized_dark(),
        }
    }

    pub fn key(&self) -> &'static str {
        match self {
            ThemeId::ObsidianSmoke => "obsidian-smoke",
            ThemeId::MidnightFrost => "midnight-frost",
            ThemeId::Bone => "bone",
            ThemeId::Phosphor => "phosphor",
            ThemeId::TokyoNight => "tokyo-night",
            ThemeId::CatppuccinMocha => "catppuccin-mocha",
            ThemeId::RosePine => "rose-pine",
            ThemeId::Dracula => "dracula",
            ThemeId::Nord => "nord",
            ThemeId::SolarizedDark => "solarized-dark",
        }
    }

    pub fn display_name(&self) -> &'static str {
        self.theme().name
    }

    pub fn from_key(key: &str) -> Option<Self> {
        Self::ALL
            .iter()
            .copied()
            .find(|theme_id| theme_id.key() == key)
    }
}

